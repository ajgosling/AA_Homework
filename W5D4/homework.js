function madlib(verb, adjective, noun) {
  return `We shall ${verb} the ${adjective} ${noun}.`
}


function isSubstring(phrase, subphrase) {
  return phrase.includes(subphrase);
}

function isPrime (n) {
  if (n < 2) { return false; }

  for (let i = 2; i < n; i++) {
    if (n % i === 0) {
      return false;
    }
  }
  return true;
}
